    private void fillReportTablel(String sql){
        
        DefaultTableModel dtm = new DefaultTableModel();
        
        try{
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            dtm.addColumn("Arrival Date");
            dtm.addColumn("Importer");
            dtm.addColumn("Departure Country");
            dtm.addColumn("HSCODE");
            dtm.addColumn("Goods Name");
            dtm.addColumn("Size");
            dtm.addColumn("Unit");
            dtm.addColumn("QTY");
            dtm.addColumn("Duty");
            
            while(rs.next()){
                Object objData[] = new Object[9];
                for(int i = 0; i<9;i++){
                    objData[i]= rs.getString(i+1);
            }
            dtm.addRow(objData);
        }
        //userTable.setModel(DbUtils.resultSetToTableModel(rs));
        
            tableHs.setModel(dtm);
        
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
            
        }
    }

