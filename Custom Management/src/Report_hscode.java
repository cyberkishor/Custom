    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        DateFormat df = new SimpleDateFormat("Y-MM-dd");
        Date d = dateFrom.getSelectedDate().getTime();
        String frmDate = df.format(d);
        
        Date td = dateTo.getSelectedDate().getTime();
        String toDate = df.format(td);
        
        String hscode = searchHscode.getText();
        
        String sql = "select `arrival_date`, `importer_code`,`country_departure`,`hscode`,`name_of_goods`,`size`,`unit`,`qty`,`duty` from import WHERE `arrival_date` between '"+frmDate +"' and '"+toDate +"' and hscode = '"+hscode+"'";
        this.fillReportTablel(sql);
        
        
        
    }//GEN-LAST:event_searchActionPerformed

