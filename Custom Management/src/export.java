    private void fillHscode(){
        try{
            String sql = "SELECT * FROM hscode order by hscode";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            //Vector model = new Vector();
             
            while(rs.next()){
                
                String code = rs.getString("goods_type");
                String name = rs.getString("hscode");
                int siz = rs.getInt("size");
                String unit = rs.getString("unit");
                int rt = rs.getInt("rate");
                
                hscode.addItem(new hsCombo(code,name,siz,unit,rt));
                
               
            }
            
        } 
        catch(Exception e){
             JOptionPane.showMessageDialog(null,e);
        }
    }
    
    
