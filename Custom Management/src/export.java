    private void valueFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_valueFocusLost
        //calculation of duty
        int qt = parseInt(qty.getText());
        hsCombo hs = (hsCombo) hscode.getSelectedItem();
        String e = "" ; 
        if(hs.getCode().toString().equals("")){
            JOptionPane.showMessageDialog(this,"Please Select hscode");
            e = "error";
        }
        if("".equals(qt)){
            JOptionPane.showMessageDialog(this,"Enter Quantity");
            e = "error";
        }
        if("".equals(e)){
            int rate = hs.getRate();
            int val = parseInt(value.getText());
            
            int dty = (qt * val * rate)/100;
            
            duty.setText("" + dty+"");
            
            
        }
        
        
    }//GEN-LAST:event_valueFocusLost


    

