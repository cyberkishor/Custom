    private void fillReportTable(String sql){
        try{
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
         table_report.setModel(DbUtils.resultSetToTableModel(rs));
    }

