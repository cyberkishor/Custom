    private void goActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_goActionPerformed
        // TODO add your handling code here:
        DateFormat df = new SimpleDateFormat("Y-MM-dd");
        String fd = df.format(date_from.getSelectedDate().getTime());
        String td = df.format(date_to.getSelectedDate().getTime());
    
        this.fillReportTable("SELECT office_code,importer_code, importer, country_departure, flight_no, passport_no from import WHERE `arrival_date` between '"+fd+ "' and '"+td+"' ");
    }//GEN-LAST:event_goActionPerformed

